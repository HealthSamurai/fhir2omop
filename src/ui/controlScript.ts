export default async function (_ctx: Context) {
    return [
        "(() => {",
        "  if (window.__hyperUiControlInstalled) return;",
        "  window.__hyperUiControlInstalled = true;",
        "  window.__hyperUiControlReady = true;",
        "",
        "  const postResult = async (payload) => {",
        "    try {",
        "      await fetch('/ui/eval/result', {",
        "        method: 'POST',",
        "        headers: { 'content-type': 'application/json' },",
        "        body: JSON.stringify(payload),",
        "      });",
        "    } catch {}",
        "  };",
        "",
        "  const actions = {",
        "    ping: async () => ({ pong: true, at: Date.now() }),",
        "    location: async () => ({ href: location.href, path: location.pathname, search: location.search, hash: location.hash }),",
        "    selectionText: async () => String(window.getSelection?.() ?? ''),",
        "  };",
        "",
        "  const notify = (ev) => {",
        "    const box = document.createElement('div');",
        "    box.className = 'fixed right-4 top-4 z-[1000] max-w-sm rounded border bg-white shadow px-3 py-2 text-sm';",
        "    if (ev.level === 'error') box.classList.add('border-red-300', 'text-red-700');",
        "    else if (ev.level === 'warn') box.classList.add('border-yellow-300', 'text-yellow-800');",
        "    else box.classList.add('border-gray-300', 'text-gray-800');",
        "    if (ev.html) box.innerHTML = ev.html;",
        "    else box.textContent = ev.message ?? ev.text ?? '';",
        "    document.body.appendChild(box);",
        "    setTimeout(() => box.remove(), 2500);",
        "  };",
        "",
        "  const handleEval = async (ev) => {",
        "    try {",
        "      const value = await (0, eval)(ev.code);",
        "      await postResult({ id: ev.id, ok: true, value });",
        "    } catch (error) {",
        "      await postResult({ id: ev.id, ok: false, error: String(error?.message ?? error) });",
        "    }",
        "  };",
        "",
        "  const handleAction = async (ev) => {",
        "    const fn = actions[ev.name];",
        "    if (!fn) {",
        "      await postResult({ id: ev.id, ok: false, error: 'unknown action: ' + ev.name });",
        "      return;",
        "    }",
        "    try {",
        "      const value = await fn(ev.args ?? null, ev);",
        "      await postResult({ id: ev.id, ok: true, value });",
        "    } catch (error) {",
        "      await postResult({ id: ev.id, ok: false, error: String(error?.message ?? error) });",
        "    }",
        "  };",
        "",
        "  const applyEvent = (ev) => {",
        "    if (!ev || typeof ev !== 'object') return;",
        "    if (ev.type === 'ui.navigate' && typeof ev.path === 'string') {",
        "      window.location.assign(ev.path);",
        "      return;",
        "    }",
        "    if (ev.type === 'ui.notify') {",
        "      notify(ev);",
        "      return;",
        "    }",
        "    if (ev.type === 'ui.eval' && typeof ev.code === 'string' && typeof ev.id === 'string') {",
        "      void handleEval(ev);",
        "      return;",
        "    }",
        "    if (ev.type === 'ui.action' && typeof ev.name === 'string' && typeof ev.id === 'string') {",
        "      void handleAction(ev);",
        "    }",
        "  };",
        "",
        "  window.__hyperUiApplyEvent = applyEvent;",
        "",
        "  if (!window.__hyperUiControlPatched && window.EventSource) {",
        "    window.__hyperUiControlPatched = true;",
        "    const NativeEventSource = window.EventSource;",
        "    window.EventSource = function (...args) {",
        "      const es = new NativeEventSource(...args);",
        "      es.addEventListener('message', (e) => {",
        "        try { applyEvent(JSON.parse(e.data)); } catch {}",
        "      });",
        "      return es;",
        "    };",
        "    window.EventSource.prototype = NativeEventSource.prototype;",
        "  }",
        "",
        "  document.addEventListener('hyper-ui-event', (e) => applyEvent(e.detail));",
        "})();",
    ].join('\n');
}
